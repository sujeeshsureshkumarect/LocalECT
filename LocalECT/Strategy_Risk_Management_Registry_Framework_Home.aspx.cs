    public partial class Strategy_Risk_Management_Registry_Framework_Home : System.Web.UI.Page
    {
        int CurrentRole = 0;
        protected void Page_Load(object sender, EventArgs e)
        {
            try
            {
                if (Session["CurrentRole"] != null)
                {
                    CurrentRole = (int)Session["CurrentRole"];
                    if (!IsPostBack)
                    {
                        if (LibraryMOD.isRoleAuthorized(InitializeModule.enumPrivilegeObjects.Risk_Management_Framework,
                        InitializeModule.enumPrivilege.ShowBrowse, CurrentRole) != true)
                        {
                            Server.Transfer("Authorization.aspx");
                        }
                    }
                }
                else
                {
                    Session.RemoveAll();
                    Response.Redirect("Login.aspx");

                }
                if (Session["CurrentUserName"] != null)
                {
                    if (!IsPostBack)
                    {
                        string id = Request.QueryString["id"];
                        if (id != null)
                        {
                            bindRisk_Management_Registry_Framework(id);
                        }
                    }
                }
            }
            catch (Exception exp)
            {
                Console.WriteLine("{0} Exception caught.", exp);
            }
            finally
            {

            }
        }

        public void bindRisk_Management_Registry_Framework(string id)
        {
            Connection_StringCLS myConnection_String = new Connection_StringCLS(InitializeModule.EnumCampus.ECTNew);
            SqlConnection sc = new SqlConnection(ConfigurationManager.ConnectionStrings["ECTDataNew"].ConnectionString);

            SqlCommand cmd = new SqlCommand("SELECT CS_Risk_Management_Registry_Framework.iSerial, CS_Risk_Management_Registry_Framework.sRegistryFramework, CS_Risk_Management_Registry_Framework.iFramework, CS_Risk_Management_Registry_Framework.dAdded, CS_Risk_Management_Registry_Framework.sAddedBy, CS_Risk_Management_Registry_Framework.dUpdated, CS_Risk_Management_Registry_Framework.sUpdatedBy, CS_Risk_Management_Framework.sFramework FROM CS_Risk_Management_Registry_Framework INNER JOIN CS_Risk_Management_Framework ON CS_Risk_Management_Registry_Framework.iFramework = CS_Risk_Management_Framework.iSerial where iFramework=@iFramework", sc);
            cmd.Parameters.AddWithValue("@iFramework", id);
            DataTable dt = new DataTable();
            SqlDataAdapter da = new SqlDataAdapter(cmd);
            try
            {
                sc.Open();
                da.Fill(dt);
                sc.Close();

                Repeater1.DataSource = dt;
                Repeater1.DataBind();
            }
            catch (Exception ex)
            {
                sc.Close();
                Console.WriteLine("{0} Exception caught.", ex);
            }
            finally
            {
                sc.Close();
            }
        }

        protected void lnk_Create_Click(object sender, EventArgs e)
        {
            string id = Request.QueryString["id"];
            Response.Redirect("Strategy_Risk_Management_Registry_Framework_Update?id=" + id + "");
        }
    }
